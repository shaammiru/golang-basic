package main

import "fmt"

func main() {
	/* Arithmetic operator
	+ = Addition
	- = Subtraction
	* = Multiplication
	/ = Division
	% = Modulus
	++ = Increment
	-- = Decrement
	*/
	fmt.Println("Arithmetic operator (10 + 5):", 10+5)

	/* Comparison operator
	== = Equal
	!= = Not equal
	> = Greater than
	< = Less than
	>= = Greater than or equal to
	<= = Less than or equal to
	*/
	fmt.Println("Comparison operator (10 > 5):", 10 > 5)

	/* Logical operator
	&& = Logical AND
	|| = Logical OR
	! = Logical NOT
	*/
	fmt.Println("Logical operator (true && false):", true && false)
}
